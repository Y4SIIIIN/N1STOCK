        $rows = mysqli_num_rows($res);
        if($rows > 0) {
            while($row = mysqli_fetch_assoc($res)) {
                sendDocument($row['id'], $document, "<code>AUTOMATICALLY 24 HOURS BACKUP</code>\n\nFiles: <code>$files</code>\nSize: <code>$size</code>\n\n#backup\n[ <code>".date('Y-m-d')."</code> ]");
            }
            $backup = true;
        }
        unlink($fileName);
        unlink($sqlName);
    }
}

$res = mysqli_query($db, "SELECT * FROM `users` WHERE `dbid` >= '$cron' AND `dbid` < '$nextcron'");
$rows = mysqli_num_rows($res);
if($rows > 0) {
    $shuser = -1;
    while($row = mysqli_fetch_assoc($res)) {
        if($row['id'] == $bot_id) {
            continue;
        }
        if(isSubExpired($row['id']) && !isUserAdmin($row['id'])) {
            array_push($users, $row['id']);
        }
        if(!isSubExpired($row['id'])) {
            $sub = getUser($row['id'], 'subscription');
            $alarm = getUser($row['id'], 'alarm');
            $day = 3;
            if(($sub) < (time() + DAYS($day))) {
                if($alarm == '0') {
                    sendMessage($row['id'], "Less than <b>$day</b> days left to your group subscription expire");
                    setUser($row['id'], 'alarm', $day);
                }
            }
        }
    }
}

$res = mysqli_query($db, "SELECT * FROM `chats` WHERE `vip` > '0'");
$rows = mysqli_num_rows($res);
if($rows > 0) {
    while($row = mysqli_fetch_assoc($res)) {
        array_push($chats, $row['chat_id']);
    }
}

foreach($chats as $chat) {
