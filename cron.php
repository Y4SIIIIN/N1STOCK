        $get = getChatMember($chat, $id);
        if(!isUserAdmin($id) && $get == 'member') {
            $mention = mentionUser($id);
            $name = bot('getChat', array('chat_id' => $chat))->result->title;
            banChatMember($chat, $id);
            sendMessage($chat, "<b>*</b> User $mention's group subscription expired. (<b>Kicked</b>)");
            sendMessage($id, "<code>*</code> You have kicked from <b>$name</b> group because of your subscription expire time");
            setUser($id, 'alarm', '0');
            setUser($id, 'double', '-1');
            $rcount ++;
        }
    }
}

